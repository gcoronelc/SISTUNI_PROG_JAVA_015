    public float categoria (){
        float bonifi_cate;
        
        bonifi_cate = 0.0f;
        if (rbtobrero.isSelected()){
            bonifi_cate = trabajador.getSuel_basi() * 0.01f;
        }else if (rbtEmpleado.isSelected()){
            bonifi_cate = trabajador.getSuel_basi() * 0.02f;
        }else if (rbtEjecutivo.isSelected()){
            bonifi_cate = trabajador.getSuel_basi() * 0.03f;
        }

        return bonifi_cate;
    }
    
    
    
    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        
        float suel_basi, suel_bruto, desc_AFP,apor_emplea,suel_neto;
        int Nhijos;
        
        suel_basi = Float.parseFloat(txtSuel_vbasi.getText().trim());
        trabajador.set_Suel_basi(suel_basi);
        
        Nhijos = Integer.parseInt(txtNhijos.getText().trim());
        trabajador.setN_hijos(Nhijos);
        
        suel_bruto = trabajador.Nhijos() + categoria() + estadocivil() + trabajador.getSuel_basi();
        desc_AFP = suel_bruto * 0.12f;
        apor_emplea = suel_bruto * 0.10f;
        suel_neto = suel_bruto - (desc_AFP + apor_emplea);
        
        lblCAte.setText(""+categoria());
        lblBoni_esta.setText(""+estadocivil());
        lblbonihijos.setText(""+trabajador.Nhijos());
        lblBruto.setText(""+suel_bruto);
        lblDescue_traba.setText(""+desc_AFP);
        lbl_apor_empela.setText(""+apor_emplea);
        lbl_neto.setText(""+suel_neto);
                
    }//GEN-LAST:event_btncalcularActionPerformed

