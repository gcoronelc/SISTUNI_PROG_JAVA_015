    public float estadocivil(){
        float bonifi_civil;
        
        bonifi_civil = 0.0f;
        if (rbtCasado.isSelected() == true){
            bonifi_civil = trabajador.getSuel_basi() * 0.02f;
        }
        else if (rbtSoltero.isSelected() == true) {
            bonifi_civil = trabajador.getSuel_basi() * 0.01f;
        }
        return bonifi_civil;
    }
    
